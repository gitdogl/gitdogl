public class OriginFileDto {
    /**文件名称*/ private String file_name;
    /**格式类型*/ private String file_type;
    /**原件类型发文：1.正文，2.发文稿纸，3.留痕件 ，3.附件 4.办理单
     收文：1.收文单；2.正文；3.附件（没有就不推）；
     正文要求为ofd格式，稿纸和留痕件、收文单要求为ofd
     */ private String order_type;
    /**文件路径*/ private String file_url;

    /**
     * 获取 文件名称
     *
     * @return file_name 文件名称
     */
    public String getFile_name() {
        return this.file_name;
    }

    /**
     * 设置 文件名称
     *
     * @param file_name 文件名称
     */
    public void setFile_name(String file_name) {
        this.file_name = file_name;
    }

    /**
     * 获取 格式类型
     *
     * @return file_type 格式类型
     */
    public String getFile_type() {
        return this.file_type;
    }

    /**
     * 设置 格式类型
     *
     * @param file_type 格式类型
     */
    public void setFile_type(String file_type) {
        this.file_type = file_type;
    }

    /**
     * 获取 原件类型
     *
     * @return order_type 原件类型
     */
    public String getOrder_type() {
        return this.order_type;
    }

    /**
     * 设置 原件类型
     *
     * @param order_type 原件类型
     */
    public void setOrder_type(String order_type) {
        this.order_type = order_type;
    }

    /**
     * 获取 文件路径
     *
     * @return file_url 文件路径
     */
    public String getFile_url() {
        return this.file_url;
    }

    /**
     * 设置 文件路径
     *
     * @param file_url 文件路径
     */
    public void setFile_url(String file_url) {
        this.file_url = file_url;
    }
}
