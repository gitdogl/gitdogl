package weaver.interfaces.jjc.oaclient.cw.dto;

/**
 * 资金来源预算
 */
public class CapitalSourceDetailDto {
    //预算id
    private int id;
    //项目id
    private int xmid;
    //预算金额
    private double je;
    //科目号
    private String kmh;
    //累加金额
    private double lje;
    //支出科目
    private String zckm;


    /**
     * 获取 预算id
     *
     * @return id 预算id
     */
    public int getId() {
        return this.id;
    }

    /**
     * 设置 预算id
     *
     * @param id 预算id
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * 获取 项目id
     *
     * @return xmid 项目id
     */
    public int getXmid() {
        return this.xmid;
    }

    /**
     * 设置 项目id
     *
     * @param xmid 项目id
     */
    public void setXmid(int xmid) {
        this.xmid = xmid;
    }

    /**
     * 获取 预算金额
     *
     * @return je 预算金额
     */
    public double getJe() {
        return this.je;
    }

    /**
     * 设置 预算金额
     *
     * @param je 预算金额
     */
    public void setJe(double je) {
        this.je = je;
    }

    /**
     * 获取 科目号
     *
     * @return kmh 科目号
     */
    public String getKmh() {
        return this.kmh;
    }

    /**
     * 设置 科目号
     *
     * @param kmh 科目号
     */
    public void setKmh(String kmh) {
        this.kmh = kmh;
    }

    /**
     * 获取 累加金额
     *
     * @return lje 累加金额
     */
    public double getLje() {
        return this.lje;
    }

    /**
     * 设置 累加金额
     *
     * @param lje 累加金额
     */
    public void setLje(double lje) {
        this.lje = lje;
    }

    /**
     * 获取 支出科目
     *
     * @return zckm 支出科目
     */
    public String getZckm() {
        return this.zckm;
    }

    /**
     * 设置 支出科目
     *
     * @param zckm 支出科目
     */
    public void setZckm(String zckm) {
        this.zckm = zckm;
    }
}
