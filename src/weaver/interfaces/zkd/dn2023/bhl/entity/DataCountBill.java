package weaver.interfaces.zkd.dn2023.bhl.entity;

public class DataCountBill {
    /**原数据标识*/
    private String origin_id;
    /**题名*/
    private String title;
    /**同步状态*/
    private int status;
    /**失败原因*/
    private String failed_msg;


    /**
     * 获取 原数据标识
     *
     * @return origin_id 原数据标识
     */
    public String getOrigin_id() {
        return this.origin_id;
    }

    /**
     * 设置 原数据标识
     *
     * @param origin_id 原数据标识
     */
    public void setOrigin_id(String origin_id) {
        this.origin_id = origin_id;
    }

    /**
     * 获取 题名
     *
     * @return title 题名
     */
    public String getTitle() {
        return this.title;
    }

    /**
     * 设置 题名
     *
     * @param title 题名
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * 获取 同步状态
     *
     * @return status 同步状态
     */
    public int getStatus() {
        return this.status;
    }

    /**
     * 设置 同步状态
     *
     * @param status 同步状态
     */
    public void setStatus(int status) {
        this.status = status;
    }

    /**
     * 获取 失败原因
     *
     * @return failed_msg 失败原因
     */
    public String getFailed_msg() {
        return this.failed_msg;
    }

    /**
     * 设置 失败原因
     *
     * @param failed_msg 失败原因
     */
    public void setFailed_msg(String failed_msg) {
        this.failed_msg = failed_msg;
    }
}
